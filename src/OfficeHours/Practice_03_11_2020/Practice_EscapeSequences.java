package OfficeHours.Practice_03_11_2020;

public class Practice_EscapeSequences {
    public static void main(String[] args) {

        System.out.println("   Java is a programming language");
        System.out.println("\tJava is a programming language");
        // \t is use for givin paragraph space

        System.out.println("\nWe love learning Java");
        System.out.println("\n\tWe love learning Java");
        // \n is use for starting new line

        System.out.println("I love the Tv Show \"The Stranger Secret\"");
        System.out.println("My favorite quote is \"Everything seems impossible until its Done\"");
        // \" used for printing double code

        System.out.println("/\\");
        // \\ is used for printing single back slash



    }

}
