package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("\t\t\tHello");
        // \t means a paragraph space (Paragraph space)

        System.out.println("Cybertek \nSchool");
        // \n Everything after this print statement print on the new line (New Line)


        System.out.println("My\nName\nIs\nSada");
        System.out.println("\nMy\nName\nIs\nSada");


        System.out.println("My favorite tv show is \"Game Of Thrones\" ");
        // \" Print Double quotes on the console

        System.out.println("My favorite book is \"How to stop livind and start livin by Dale Carnegie\"");

        System.out.println("\\");
        // \\ prints single slash on the console
        System.out.println("/");

        System.out.println("My Favorite program is \'Java\'");
        // ', \' prints single quote to the screen



    }
}
