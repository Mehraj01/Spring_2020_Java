package SELF_PRACTICE.Solutionns_Classes_Ways;

public class Alphabets_Ascii_For_Loop {
    public static void main(String[] args) {
         /*
        Print all lower case and upper case alphabets from the ASCII table
         */
         // a~z, A~Z

        for(char ch='a'; ch<='z'; ch++){
            System.out.print(ch+" ");
        }

        System.out.println();

        for(char ch='A'; ch<='Z'; ch++){
            System.out.print(ch+" ");
        }






    }
}
