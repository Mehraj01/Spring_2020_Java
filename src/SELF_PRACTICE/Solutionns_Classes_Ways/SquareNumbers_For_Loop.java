public class SquareNumbers_For_Loop {
    public static void main(String[] args) {

        for(int i=1; i<10; i+=2){
            System.out.println("Value of i is: "+i);
        }
         /*
        Tas01:
        print the square of the numbers
         */

        for (int a = 1; a <= 2; a++) {
            System.out.println("The square of " + a + " is: " + (a * a));
        }

    }
}
